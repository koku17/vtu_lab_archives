package Program7;

// Program 7 - Primitive Data Types
public class Program7 {
	public static void main(String[] args) {
		byte bt=100;			// byte can store whole nos between -128 to 127
		short st=5000;			// short can store whole numbers from -32768 to 32767
		int myNum=100000;		// integer can store whole nos between -2147483648 to 2147483647
		float myFloatNum=5.99f;		// floating point number can store between 3.4e−038 to 3.4e+038
		char myLetter='D';		// character 
		boolean myBool=true;		// boolean
		double myDub=19.99d;		// double can store between 1.7e−308 to 1.7e+308 
		String myText="Hello";		// String	
		System.out.println(bt);
		System.out.println(st);
		System.out.println(myNum);
		System.out.println(myFloatNum);
		System.out.println(myLetter);
		System.out.println(myBool);
		System.out.println(myText);
		System.out.println(myDub);
	}
}
